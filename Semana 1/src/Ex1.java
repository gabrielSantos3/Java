public class Ex1 {
    public static void main(String[] args) {
        String txt = "Gabriel Santos";
        
        // O método toUpperCase() transfoma o texto da String em maiúsculo
        System.out.println("Texto todo maiúsculo: "+txt.toUpperCase());

        // O método toLowerCase() transfoma o texto da String em minúsculo
        System.out.println("Texto todo minúsculo: "+txt.toLowerCase());
    }
}
